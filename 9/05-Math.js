
    var num = -0.6;
    console.log(Math.abs(num));        //0.6 取绝对值
    console.log(Math.floor(num));      //-1 向下取整,向小取
    console.log(Math.ceil(num));       //-0 向上取整,向大取
    console.log(Math.round(num));      //-1 四舍五入取整，正数四舍五入，负数五舍六入
    console.log(Math.round(-0.5));     //-0
    console.log(Math.random());        //0.746932625934539 随机数0-1
    console.log(Math.sqrt(4));         //2
